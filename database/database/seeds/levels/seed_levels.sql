-- seed_levels.sql
-- GemStone IV experience thresholds per level.
-- XP required is cumulative total to REACH that level.
--
-- Formula from wiki:
--   Levels  1-15: each level costs 10,000 XP  (+10k per level, starts at 0)
--   Levels 16-25: each level costs  8,000 XP
--   Levels 26-40: each level costs  6,000 XP
--   Levels 41-50: each level costs  4,000 XP
--   Levels 51-100: each level costs 2,000 XP
--
-- Level 1 = 0 XP (starting level)
-- Level 2 = 10,000
-- Level 3 = 20,000
-- ...
-- Level 15 = 140,000
-- Level 16 = 152,000  (140k + 8k more after transition... see values below)

USE gemstone_dev;

CREATE TABLE IF NOT EXISTS levels (
    level           SMALLINT UNSIGNED PRIMARY KEY,
    xp_required     BIGINT UNSIGNED NOT NULL,    -- cumulative XP to reach this level
    xp_to_next      INT UNSIGNED NOT NULL,        -- XP cost of this level
    tp_physical     TINYINT UNSIGNED DEFAULT 35, -- training points gained on level-up
    tp_mental       TINYINT UNSIGNED DEFAULT 35
) ENGINE=InnoDB;

TRUNCATE TABLE levels;

